class Solution(object):
    def findCircleNum(self, isConnected):
        """
        :type isConnected: List[List[int]]
        :rtype: int
        """
        n = len(isConnected)
        root = [i for i in range(n)]
        
        def find(x):
            return root[x]
        
        for (x, city) in enumerate(isConnected):
            for (y , connection) in enumerate(city):
                if connection == 1  and x != y:
                    root_x = find(x)
                    root_y = find(y)
                    if root_x != root_y:
                        for i in range(n):
                            if root[i] == root_y:
                                root[i] = root_x
                    
    
           
        return len(set(root))
      
        
